        x_search_endpoint="https://api.x.com/2/tweets/search/recent",
        x_max_results_per_page=100,
        x_pages_per_query=5,
        x_min_request_interval_seconds=0.0,
        x_lookback_days=1,
        x_max_backfill_days=1,
        x_window_hours=12,
