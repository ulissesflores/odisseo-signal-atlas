    def fetch_repo(self, owner: str, repo: str) -> dict[str, Any]:
        return {
            "html_url": f"https://github.com/{owner}/{repo}",
            "stargazers_count": 77,
            "language": "Python",
            "description": "Frontier MCP memory stack.",
            "topics": ["mcp", "memory", "agents"],
            "updated_at": "2026-03-19T00:00:00Z",
            "pushed_at": "2026-03-18T00:00:00Z",
            "homepage": "https://ulissesflores.com",
            "default_branch": "main",
            "license": {"name": "MIT"},
            "open_issues_count": 3,
            "forks_count": 4,
            "archived": False,
        }

    def fetch_readme(self, owner: str, repo: str) -> str:
        return "# Atlas\nMemory, agent orchestration, MCP hooks, voice RAG."

    def fetch_languages(self, owner: str, repo: str) -> dict[str, int]:
        return {"Python": 1500, "TypeScript": 300}

    def close(self) -> None:
        return None


